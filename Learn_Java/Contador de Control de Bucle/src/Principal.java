//Contador de control de bucle.
public class Principal {
			
	public static void main(String[] args) {
				int c=0;
				do {
					//Ordenes que necesitemos dentro del bucle, en este caso ir sumando uno y mostrar.
					c=c+1;
					System.out.println(c);
				}
				//Ejecutará el bucle mientras que no se cumpla la condición establecida.
				while (c<10);
			}
		}


