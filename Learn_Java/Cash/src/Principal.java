//Conversion explicita.
public class Principal {

	public static void main(String[] args) {
		//Declaramos las variables.
		double b=53.45;
		//Convertimos el resultado de 52*b a tipo float.
		float c=(float) (52*b);
		//Escribimos por pantalla el valor de la variable que precisemos.
		System.out.println(c);
	}
}
