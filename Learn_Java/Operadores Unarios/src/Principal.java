
public class Principal {

	public static void main(String[] args) {
		int a = -25;
		int b = 36;
		//Utilizamos  para cambiar el signo de "-25"	
		System.out.println(-a);
		
		//Con b++ sumamos 1 a nuestro valor b.
		b++;
		System.out.println(b);
		
		//Con b-- restamos uno al valor que tenemos actualmente de b.
		b--;
		System.out.println(b);

	}

}
