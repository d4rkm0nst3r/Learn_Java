//Conversion Implicita
public class Principal {

	public static void main(String[] args) {
		//Declaramos las variable.
		int x=5;
		//Al aplicar el operador + sobre una cadena y un valor de tipo int,
		//de manera implícita la máquina virtual de Java ha convertido el valor de x en texto
		//y ha aplicado la concatenación.
		System.out.println("El valor de x es " + x);

	}

}
